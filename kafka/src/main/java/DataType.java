public enum DataType {
    /**
     * true/false
     */
    BOOLEAN,
    /**
     * 4 byte integer
     */
    INT,
    /**
     * 8 byte integer
     */
    BIGINT,
    /**
     * single precision
     */
    FLOAT,
    /**
     * double precision
     */
    DOUBLE,
    /**
     * sequence of characters
     */
    STRING,
    /**
     * binary
     */
    BLOB,
}
